CREATE DATABASE IF NOT EXISTS `it_asset_db` CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE `it_asset_db`;

-- ====================================================
-- Tabel: assets
-- ====================================================
DROP TABLE IF EXISTS `lendings`;
DROP TABLE IF EXISTS `assets`;

CREATE TABLE `assets` (
    `id` INT AUTO_INCREMENT PRIMARY KEY,
    `category` VARCHAR(50) NOT NULL,
    `name` VARCHAR(150) NOT NULL,
    `material` VARCHAR(50) DEFAULT '',
    `brand` VARCHAR(100) DEFAULT '',
    `code` VARCHAR(50) DEFAULT '',
    `qty` INT DEFAULT 1,
    `good` TINYINT(1) DEFAULT 1,
    `broken` TINYINT(1) DEFAULT 0,
    `pic` VARCHAR(100) NOT NULL,
    `for_sale` TINYINT(1) DEFAULT 0,
    `obsolete` TINYINT(1) DEFAULT 0,
    `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    `updated_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- ====================================================
-- Tabel: lendings
-- ====================================================
CREATE TABLE `lendings` (
    `id` INT AUTO_INCREMENT PRIMARY KEY,
    `asset_id` INT NOT NULL,
    `borrower` VARCHAR(100) NOT NULL,
    `department` VARCHAR(100) NOT NULL,
    `lend_date` DATE NOT NULL,
    `due_date` DATE NOT NULL,
    `return_date` DATE DEFAULT NULL,
    `notes` TEXT DEFAULT NULL,
    `status` ENUM('active','returned') DEFAULT 'active',
    `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (`asset_id`) REFERENCES `assets`(`id`) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

